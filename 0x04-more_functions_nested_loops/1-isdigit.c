/**
 * _isdigit - Checks for a digit
 *
 * Description: Function that checks for a digit
 *
 *@c: The character to check.
 *
 * Return: 1 if c is a digit, 0 otherwise
 */

int _isupper(int c)
{
	if (c > 47 && c < 58)
		return (1);
	else
		return (0);
	return (0);
}

